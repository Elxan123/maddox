                                var addressFormatting = function(text){
                                    var newText = text;
                                    //array of find replaces
                                    var findreps = [
                                        {find:/^([^\-]+) \- /g, rep: '<span class="ui-selectmenu-item-header">$1</span>'},
                                        {find:/([^\|><]+) \| /g, rep: '<span class="ui-selectmenu-item-content">$1</span>'},
